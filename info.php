                <h3>Okay, but why Pinchard's Island?</h3>

                <p>I have been photographing <a href="http://adamsim.ms/pinchards-island/" target="_blank">Pinchard’s Island</a> and it’s previous residents for several years, but the harsh conditions and the extreme remoteness of this island made it impossible to take images over longer periods of time during different seasons.  Cloudberry grew from the desire to be able to photograph the island throughout the year.</p>

                <img src="https://static1.squarespace.com/static/50e5fc10e4b0291e3b9b75c6/588f654e725e2506f4c22cd9/588f68e56a4963c410121736/1485793514975/10_adam_simms_sisters.jpg" class="img-responsive info_img" alt="Pinchard's Island Sisters" />

                <p>Shortly after Newfoundland joined Canada as it’s 10th province, Pinchard’s Island was resettled in an attempt to modernize the province. I have been documenting the return of my grandmother, along with her brothers and sisters, to this island each summer in an attempt to rewrite the history of resettlement by reviving old ways of life and create new traditions.</p>
