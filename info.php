                <h3>Installation</h3>
                <p>During the second week of August, we embarked on our journey to install Cloudberry. The first task was to bring all of the solar power components to the island, which was a task that required four people to load the housing unit, batteries, and solar panel. It took approximately two days for Roger and I to install the entire system with constant readjustments. We had to balance cable lengths, accessibility, architecture of the cabin, and predict the potential weather problems. Based on our research of solar irradiance for the geocordinates of the cabin, we decided to tilt the solar panel at an angle of 20 degrees since the summer was coming to an end and the winter months would prove to be the most challenging for sunlight. We had to position the solar panel with the architecture of the roof and slightly over the heave to prevent the build up of ice and snow, but this was risky since it was difficult to tightly secure the panel to the roof in the event of high winds and slightly positions the panel out of the sun’s path.</p>

                <img src="img/info/solar-install.jpg" class="img-responsive info_img" alt="Solar Power Install" />

                <p>The second step was to choose the frame of the photograph and install the Cam Do enclosure. Prior to arriving at the island I was confused about what would be the best thing to capture; the landscape, the ocean, something else entirely. I was reliant on the outside structure of the cabin to orient the frame and through a process of elimination I was able to make a decision. Pointing the camera towards the south was directly inline with the path of the sun causing the image to be blasted throughout the day. The east and the west also was affected by the same blasting in the morning or the evening and only offered views of the landscape. Pointing the camera towards the north avoids the blasting issue and offers the view of both the landscape and the ocean, which seems to offer more diverse photographs. This frame also felt right since the placement of the camera is near the north facing window and reminds me of the inside view from the cabin window.</p>

                <img src="img/info/cam-do.jpg" class="img-responsive info_img" alt="Cam Do Enclosure" />

                <p>Next, it was time to assemble the camera components together and test. We connected the Cam Do interval timer to the GoPro HERO4, and powered camera via USB cables plugged into the solar power. The new USB cords we introduced were causing the camera to detect a USB power mode when the camera was turning on, which conflicted with the `autoexe` script that we wrote to take the photo, turn on WiFi and put the camera in standby mode. After half a day of troubleshooting, we had to rewrite the script to re-sequence the code to control the camera and go through a series of tests that component.</p>

                <p>Once we stabilized the camera component, we introduced the Raspberry Pi and it’s add-ons. Initial test had this component of the system performing well; the WittyPi was booting and shutting down the Raspberry Pi on schedule, the Raspberry Pi was connecting to the GoPro HERO4 WiFi enabling us to access the camera and the USB hotspot was providing internet to upload the images to the AWS S3 Bucket. However, once we settled the Raspberry Pi and it’s add-ons in the Nanuk case, it significantly impacted the WiFi signal to the point where we were not able to maintain a decent connection to the GoPro HERO4. This resulted in us completely rewiring the power to this component of the system to allow the Raspberry Pi be positioned as close to the camera as possible to improve the WiFi signal strength.</p>

                <img src="img/info/pi.jpg" class="img-responsive info_img" alt="Cloudberry" />

                <p>Once the entire system was in place, we monitored everything for an entire day before locking up all of the cases and leaving the island. It took us an entire week to put the system in place.</p>
