              <div class = "col-sm-8 col-sm-offset-2">

                <h4><strong>GoPro HERO4 Black with 16gb micro SD Card.</strong></h4>
                <p>We initially wanted to use the GoPro HERO5, but Cam Do enclosure did not support the GoPro HERO5. Creating a DIY weatherproof enclosure didn’t add any benefit since the image quality between GoPro HERO4 and GoPro HERO5 is the same; therefore, the decision was made to go with the GoPro HERO4.</li>

                <h4><strong>Cam Do Blink Interval Timer</strong></h4>
                <p>The interval timer turns on the GoPro HERO4 every hour between 8 AM and 8 PM. The GoPro runs an `autoexe` script upon power on that commands the camera to take a photo, turn on the GoPro WiFi and then puts the camera in standby mode to conserve power. In the event that something goes wrong, such as no power for a period of time, the interval timer will attempt turn on the camera everyday when there is power and enable WiFi so that we can control the camera remotely via the Raspberry Pi.</li>

                <h4><strong>Raspberry Pi 3 Model B</strong></h4>
                <p>Standard Raspberry Pi with a Noobs SD Card running Raspbian OS.</li>

                <h4><strong>WittyPi</strong></h4>
                <p>A real time clock (RTC) that turns on the Raspberry Pi night for 30 minutes. The Raspberry Pi runs a Python script that:
                		1. Wakes up the GoPro via WiFi.
                		2. Downloads and deletes the latest images from the GoPro.
                		3. Uploads the images to an Amazon Web Services S3 Bucket and deletes the images from the Raspberry Pi.
                		4. Puts the GoPro into standby mode.
                		5. Shuts down the Raspberry Pi to conserve power.</li>

                <h4><strong>Arduino Pro Mini + High Side DC Current Sensor Breakout Board</strong></h4>
                <p>We added a voltage meter to monitor the current power supply to avoid power issues that might corrupt the SD Card. In the event that the power supply is low, the Ardunio triggers the WittPi to shut down the Raspberry Pi until there is sufficient battery to power the device.</li>

                <h4><strong>WatchDog</strong></h4>
                <p>Since the Raspberry Pi is managed remotely the WatchDog monitors the health of the Raspberry Pi and automatically restarts the system in the event of a malfunction. </li>

                <h4><strong>HAUWEI LTE E8372 USB Hotspot</strong></h4>
                <p>A USB hotspot connected to the Raspberry Pi USB port with a SIM card and mobile internet plan from Bell Canada. This allows us to connect to the internet via USB and reserve the Raspberry Pi to connect to the GoPro HERO4 via WiFi. At ±6mb per photo, 13 photos per day, we’re using ±2.3gb per month.</li>

                <h4><strong>USB cables, extensions, and repeators</strong></h4>
                <p>All cables were weatherproof with plastic tubbing and electrical tape.</p>

                <h4><strong>Cam Do Weatherproof Enclosure</strong></h4>
                <p>Home of the GoPro HERO4. We made several modifications to the enclosure:
                <ul>
                  <li>The case has an opening on the top, which seemed problematic considering the harsh environment. We deduced to silicone the entire top opening to avoid potential leaking.</li>
                  <li>We drilled a whole in the bottom of the case to connect a USB cord and a repeater to power the GoPro. We then siliconed the USB cable and the entire opening of the hole to prevent potential leaking.</li>
                  <li>The enclosure comes with a standard screw on clear camera filter attached to the opening for the GoPro lens. We siliconed the entire edge of the filter to prevent potential leaking.</li>
                  <li>The first few days we were getting condensation on the filter preventing visibility to the GoPro lens. We decided to remove the air pressure filter of the Cam Do case in an attempt to allow more air flow. Since this modification the filter did not accumulate any further condensation. </li>
                </ul>

                <h4><strong>Nanunk Case</strong></h4>
                <p>Holds and insulates the Raspberry Pi with add-ons and the USB hotspot.

                <h4><strong>130 Watt Solar Power System</strong></h4>
                <p>We worked with Newfound Energies, Inc. in St. John’s Newfoundland to help design a solar panel system that would intermittently power the system we were designing. Because Pinchard’s Island receives approximately 1-1.5 hours of sunlight during the winter, we decided to go with a larger solar power system in hopes that it would be enough to power the system during the winter, allow for the charging of other devices (laptops, phones, etc) during the summer, and an investment in system that could be used for future projects.

                <ul>
                  <li>130-watt solar panel pointed at ±20 degrees towards the path of the sun during the winter. Adjusted to ±60 degrees in the summer.</li>
                  <li>40 amp solar charge controller</li>
                  <li>1500-watt inverter</li>
                  <li>Four 6 volt, 385 amp hour deep cycle batteries</li>
                  <li>30 amp automatic transfer switch</li>
                  <li>30 amp battery charger</li>
                </ul>

                <h4><strong>DIY Weatherproof Box</strong></h4>
                <p>We built an insulated, weatherproof housing unit for the solar power system.</p>

                <h4><strong>Dataplicity</strong></h4>
                <p>Allows us to connect to the Raspberry Pi remotely via the command line interface.</p>

                <h4><strong>Amazon Web Services</strong></h4>
                <p>S3 Bucket for the storing the photographs, CloudFront for delivering the content.</p>

                <h4><strong>KonradIT's GoPro HERO4 WiFi Hacks</strong></h4>
                <p>A series of wifi commands that enable to to control the GoPro HERO4.</p>
              </div>
