                <h3>How?</h3>

                <p>Angela and I met early May 2017 to briefly discuss the possibility of collaborating together on a project. The idea was loose, but the goal was to take photos of the island remotely, upload the images via the cellular network and access them from anywhere. We both shared connections to Newfoundland and a passion for art and technology so we set out to see what was possible.</p>

                <p>Our initial research showed that there were a lot of possibilities as to how we could approach the project, but it was clear from the beginning that every decision would result of many constraints that would affect every decision–one after another. Power, temperature, weather, sunlight, data limits, storage, remoteness, were all components that constantly determined the decisions we made and the methods in which we worked.<p>

                <p>We used <a href="http://www.trello.com/" target="_blank">Trello</a> to plan every aspect of the project, communicate, and document ongoing research:<p>

