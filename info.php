                <h3>So, what is this?</h3>
                <p><strong>Cloudberry</strong> is an off the grid, solar powered, long term photography project. Using a GoPro, a Raspberry Pi and a USB hotspot, the system we designed takes one photograph per hour between 8am and 8pm each day and automatically uploads the images via a LTE network to this website.</p>

                <p>The photographs depict a view of Pinchard’s Island from a small, family owned cabin named “Precious Moments.” Pinchard's Island, only accessible by boat for a few weeks of the year, is home to a few cabins that resettled residents use while picking bake apples (the local term for cloudberries) during the summer months.</p>

                <p>The view is static–in the sense that camera is always capturing the same frame; however, the lighting of the frame can vary drastically from one image to another. I see these photographs as a continuation of the moments I spent in the cabin glancing out the window at the surrounding landscape. I am interested in the visual transition of time and light. I am curious about the notion of watching and accessing a place without physically being present.</p>
